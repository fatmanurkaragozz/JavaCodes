public class IciceDonguOrneklerii {
    public static void main(String[] argjs) {
        int i = 1;
        while (i < 8) {
            for (int j = i; j > 0; j--) {
                System.out.print(j + "  ");
            }
            System.out.println();
            i++;
        }

        int a = 7;
        while (a > 0) {
            for (int b = a; b > 0; b--) {
                System.out.print(b + "  ");
            }
            System.out.println();
            a--;
        }
    }
}
