public class WhileFor {
    public static void main(String[] args) {

        int i = 1;
        while(i < 101 ){
            System.out.println("Yazilim Muhendisligi-" + i);
            i++;
        }
        System.out.println("-------------------------");

        for(int j = 1; j < 101; j++){
            System.out.println("Yazilim Muhendisligi-" + j);
        }

        for(int a = 1; a < 101; a++){
            System.out.println("Yazilim Muhendisligi-" + a);
            System.out.println("Cok calismaliyiz cokk-" + a);

        }
    }
}
